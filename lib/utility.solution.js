exports.navToLink = function(req, res) {
  Link.findOne({ code: req.params[0] }).exec(function(err,link) {
    if (!link) {
      res.redirect('/');
    } else {
      link.visits++;
      link.save(function(err,link){
        res.redirect(link.url);
        return;
      })
    }
  });
};

